def _header(
    title: str,
    material_id: str,
    extra: list[str] | None = None,
    mode: str = "static",
) -> list[str]:
    out = [
        f"; {title}",
        f"; generated by emitter.py",
    ]
    if extra:
        out += [f"; {e}" for e in extra]
    out += [
        ";",
        ";HEAD: laser",
        f";MATERIAL: {material_id}",
    ]
    if mode == "static":
        out.append(
            ";LASER_MODE: static  (M3 — dwell-burn-through risk if motion stalls)"
        )
    out += [
        "",
        *_PREAMBLE,
    ]
    return out



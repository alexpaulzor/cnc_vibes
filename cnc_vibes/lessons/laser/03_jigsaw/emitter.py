def load_material(material_id: str) -> dict:
    """Look up a material by id in profiles/laser_materials.yaml."""
    path = REPO_ROOT / "profiles" / "laser_materials.yaml"
    with path.open() as f:
        materials = yaml.safe_load(f)
    for m in materials:
        if m.get("id") == material_id:
            return m
    available = ", ".join(sorted(m.get("id", "?") for m in materials))
    raise SystemExit(f"unknown material: {material_id}. Available: {available}")


# ---------------------------------------------------------------------------
# Coord conversion (image px Y-down → machine mm Y-up)
# ---------------------------------------------------------------------------


def img_to_machine_mm(
    x_px: float, y_px: float, cfg: PuzzleConfig
) -> tuple[float, float]:
    """Image pixel coords (panel inset by margin_px, Y-down) to machine
    mm (panel at origin, Y-up). cfg.origin_offset_mm shifts the result —
    set it to (panel_mm/2, panel_mm/2) to center the panel on WCS origin
    instead of pinning the bottom-left corner there."""
    x_mm = (x_px - cfg.margin_px) / cfg.px_per_mm - cfg.origin_offset_mm[0]
    y_mm = (
        cfg.panel_height_mm
        - (y_px - cfg.margin_px) / cfg.px_per_mm
        - cfg.origin_offset_mm[1]
    )
    return (x_mm, y_mm)


# ---------------------------------------------------------------------------
# GCode header / preamble
# ---------------------------------------------------------------------------


_PREAMBLE = (
    "$32=1   ; GRBL laser mode",
    "G21     ; mm",
    "G90     ; absolute",
    "M5      ; laser off",
    "G0 X0 Y0",
    "",
)


def _header(
    title: str,
    material_id: str,
    extra: list[str] | None = None,
    mode: str = "dynamic",
) -> list[str]:
    out = [
        f"; {title}",
        f"; generated by lessons/laser/03_jigsaw/emitter.py",
    ]
    if extra:
        out += [f"; {e}" for e in extra]
    out += [
        ";",
        ";HEAD: laser",
        f";MATERIAL: {material_id}",
    ]
    if mode == "static":
        out.append(
            ";LASER_MODE: static  (M3 — dwell-burn-through risk if motion stalls)"
        )
    out += [
        "",
        *_PREAMBLE,
    ]
    return out


# ---------------------------------------------------------------------------
# Cut GCode: simple (per-polygon, no dedup) — for small puzzles
# ---------------------------------------------------------------------------


def _polygon_to_paths_mm(poly, cfg: PuzzleConfig) -> list[list[tuple[float, float]]]:
    """Exterior + holes of one polygon, in machine mm. Handles
    MultiPolygon / GeometryCollection recursively."""
    paths = []
    if isinstance(poly, Polygon):
        ext = [img_to_machine_mm(x, y, cfg) for x, y in poly.exterior.coords]
        paths.append(ext)
        for interior in poly.interiors:
            paths.append([img_to_machine_mm(x, y, cfg) for x, y in interior.coords])
    elif isinstance(poly, (MultiPolygon, GeometryCollection)):
        for sub in poly.geoms:
            if isinstance(sub, Polygon):
                paths.extend(_polygon_to_paths_mm(sub, cfg))
    return paths


def order_inside_out(pieces: list[dict]) -> list[dict]:
    """Letter pieces first (inside the cell pockets), then cells.
    Simple ordering for small puzzles; for full puzzles use the
    edge-based toposort below."""
    letters = [p for p in pieces if p.get("kind") == "letter"]
    cells = [p for p in pieces if p.get("kind") != "letter"]
    return letters + cells


def decimate_min_segment(
    pts: list[tuple[float, float]], min_seg_mm: float
) -> list[tuple[float, float]]:
    """Drop intermediate points that would create a segment shorter than
    min_seg_mm. Endpoints are always preserved (so closed rings stay
    closed). Guarantees every emitted segment is >= min_seg_mm, except a
    degenerate path that collapses to its two endpoints."""
    if min_seg_mm <= 0 or len(pts) < 3:
        return pts
    out = [pts[0]]
    for p in pts[1:]:
        lx, ly = out[-1]
        if math.hypot(p[0] - lx, p[1] - ly) >= min_seg_mm:
            out.append(p)
    # Force-preserve the final endpoint. If it was skipped because it sat
    # within min_seg of the last kept point, drop that kept point so the
    # closing segment is still >= min_seg.
    if out[-1] != pts[-1]:
        if len(out) >= 2:
            out.pop()
        out.append(pts[-1])
    return out


def emit_cut_gcode_simple(
    pieces: list[dict],
    material: dict,
    cfg: PuzzleConfig,
    word: str,
    mode: str = "dynamic",
    warmup_ms: int = 0,
    feed_override: int | None = None,
    min_segment_mm: float = 0.0,
    power_percent: float | None = None,
    warmup_ms_first: int | None = None,
) -> str:
    """Per-polygon cut, simple letter-then-cells ordering. Suitable for
    small-piece-count puzzles where edge dedup isn't worth the complexity.
    Shared cell-to-cell edges get cut twice; acceptable for ~5-piece
    tests.

    mode="dynamic" (default) emits M4 (power scales with feed); "static"
    emits M3 + a ;LASER_MODE: static header (constant power). warmup_ms>0
    inserts a G4 dwell after the laser turns on at the start of each path,
    defeating diode cold-start fade-in (dial it in with lesson 06's
    `--sweep warmup`). feed_override replaces the material feedrate.
    min_segment_mm decimates points so no emitted segment is shorter than
    that distance. power_percent overrides the material power (S =
    percent*10)."""
    laser = material["laser"]
    pct = power_percent if power_percent is not None else laser["power_percent"]
    power_s = int(round(pct * 10))
    feed = feed_override if feed_override is not None else laser["feed_mm_per_min"]
    passes = laser["passes"]
    on = "M3" if mode == "static" else "M4"
    warmup_s = max(0, warmup_ms) / 1000.0
    first_ms = warmup_ms if warmup_ms_first is None else warmup_ms_first
    first_s = max(0, first_ms) / 1000.0

    extra = [
        "loose-fit puzzle: centerline cuts, kerf becomes the clearance",
        "ASSUMES Z already at focal height in your WCS",
        "cut order: letters first, then cells (simple; shared edges cut twice)",
    ]
    if first_ms > 0 or warmup_ms > 0:
        extra.append(
            f"warmup: G4 P{first_s:.3f} on first cut, P{warmup_s:.3f} on the rest "
            "(diode cold-start)"
        )
    if min_segment_mm > 0:
        extra.append(f"min segment: {min_segment_mm}mm (shorter chords decimated)")

    ordered = order_inside_out(pieces)
    lines = _header(
        title=f"small puzzle: word={word}, {len(pieces)} pieces, "
        f"{cfg.panel_mm:.0f}x{cfg.panel_mm:.0f}mm",
        material_id=material["id"],
        extra=extra,
        mode=mode,
    )

    first_cut = True
    for i, piece in enumerate(ordered, start=1):
        paths = _polygon_to_paths_mm(piece["polygon"], cfg)
        kind = piece.get("kind", "cell")
        for path_idx, pts in enumerate(paths):
            pts = decimate_min_segment(pts, min_segment_mm)
            if len(pts) < 3:
                continue
            label = f"{kind} {i}"
            if path_idx > 0:
                label += f" (hole {path_idx})"
            lines.append(f"; --- {label} ---")
            x0, y0 = pts[0]
            lines.append(f"G0 X{x0:.3f} Y{y0:.3f}")
            lines.append(f"{on} S{power_s}")
            lines.append(f"F{feed}")
            dwell_s = first_s if first_cut else warmup_s
            if dwell_s > 0:
                lines.append(f"G4 P{dwell_s:.3f}  ; warmup")
            first_cut = False
            for pass_n in range(passes):
                if passes > 1:
                    lines.append(f"; pass {pass_n + 1} of {passes}")
                for x, y in pts[1:]:
                    lines.append(f"G1 X{x:.3f} Y{y:.3f}")
            lines.append("M5")
            lines.append("")
    lines += ["G0 X0 Y0", ""]
    return "\n".join(lines)


# ---------------------------------------------------------------------------
# Cut GCode: edge-dedup with containment-aware ordering — for full puzzle
# ---------------------------------------------------------------------------


def extract_unique_edges(pieces: list[dict]) -> list[LineString]:
    """unary_union dedupes shared edges; linemerge combines collinear
    connected segments into continuous chains. Each interior edge
    appears exactly once in the output."""
    boundaries = [p["polygon"].boundary for p in pieces]
    merged = unary_union(boundaries)
    if isinstance(merged, (MultiLineString, GeometryCollection)):
        segs = [g for g in merged.geoms if isinstance(g, LineString)]
    elif isinstance(merged, LineString):
        segs = [merged]
    else:
        segs = []
    if len(segs) <= 1:
        return segs
    chained = linemerge(MultiLineString(segs))
    if isinstance(chained, LineString):
        return [chained]
    if isinstance(chained, MultiLineString):
        return list(chained.geoms)
    return [g for g in chained.geoms if isinstance(g, LineString)]


def classify_edge(
    edge: LineString,
    letter_polys: list[Polygon],
    cfg: PuzzleConfig,
    eps: float = 0.5,
) -> str:
    """Return 'letter', 'panel', or 'interior'. Panel edges lie on the
    panel rectangle; letter edges lie on a letter polygon's boundary;
    everything else is interior cell-to-cell."""
    coords = list(edge.coords)
    panel_x0 = cfg.margin_px
    panel_y0 = cfg.margin_px
    panel_w = cfg.puzzle_w_px
    panel_h = cfg.puzzle_h_px
    on_left = all(abs(x - panel_x0) < eps for x, _ in coords)
    on_right = all(abs(x - (panel_x0 + panel_w)) < eps for x, _ in coords)
    on_top = all(abs(y - panel_y0) < eps for _, y in coords)
    on_bottom = all(abs(y - (panel_y0 + panel_h)) < eps for _, y in coords)
    if on_left or on_right or on_top or on_bottom:
        return "panel"
    for lp in letter_polys:
        try:
            if edge.distance(lp.boundary) < eps:
                return "letter"
        except Exception:
            continue
    return "interior"


def greedy_order(
    edges: list[LineString], start_pt: tuple[float, float] = (0.0, 0.0)
) -> list[tuple[LineString, bool]]:
    """O(n²) greedy nearest-endpoint TSP. Returns (edge, reversed) per
    pick so the caller knows direction."""
    remaining = list(range(len(edges)))
    ordered: list[tuple[LineString, bool]] = []
    cur = start_pt
    while remaining:
        best_idx = None
        best_dist = float("inf")
        best_reverse = False
        for i in remaining:
            e = edges[i]
            c0 = e.coords[0]
            cn = e.coords[-1]
            d0 = (c0[0] - cur[0]) ** 2 + (c0[1] - cur[1]) ** 2
            dn = (cn[0] - cur[0]) ** 2 + (cn[1] - cur[1]) ** 2
            if d0 < best_dist:
                best_dist = d0
                best_idx = i
                best_reverse = False
            if dn < best_dist:
                best_dist = dn
                best_idx = i
                best_reverse = True
        e = edges[best_idx]
        coords = list(e.coords)
        if best_reverse:
            coords = coords[::-1]
            ordered.append((LineString(coords), True))
        else:
            ordered.append((e, False))
        cur = coords[-1]
        remaining.remove(best_idx)
    return ordered


def chain_contiguous_paths(
    ordered: list[tuple[LineString, bool]], tol_px: float = 0.5
) -> list[list[tuple[float, float]]]:
    """Fuse an ordered list of (edge, reversed) into continuous polylines.

    Consecutive edges whose join is within tol_px are concatenated into a
    single path so the laser cuts them in one uninterrupted stroke (one
    laser-on, one warmup dwell, no mid-line lift/re-fire). A new path
    starts only where the gap to the previous edge's end exceeds tol_px.

    Returns a list of point-lists (each a continuous path). Each edge's
    direction was already resolved by greedy_order; we honor it.
    """
    paths: list[list[tuple[float, float]]] = []
    cur: list[tuple[float, float]] = []
    for edge, _reversed in ordered:
        pts = list(edge.coords)
        if len(pts) < 2:
            continue
        if not cur:
            cur = list(pts)
            continue
        lx, ly = cur[-1]
        if math.hypot(pts[0][0] - lx, pts[0][1] - ly) <= tol_px:
            # contiguous — append, skipping the duplicated join point
            cur.extend(pts[1:])
        else:
            paths.append(cur)
            cur = list(pts)
    if cur:
        paths.append(cur)
    return paths


def emit_cut_gcode_full(
    pieces: list[dict],
    material: dict,
    cfg: PuzzleConfig,
    word: str,
    mode: str = "dynamic",
    warmup_ms: int = 0,
    feed_override: int | None = None,
    min_segment_mm: float = 0.0,
    power_percent: float | None = None,
    warmup_ms_first: int | None = None,
) -> str:
    """Full-panel cut emission with edge dedup + containment-aware
    ordering. Shared cell-cell boundaries cut exactly once. Cut order:
    letter perimeters → interior cell-to-cell → panel border last (so
    stock stays attached until the very final cut).

    mode / warmup_ms / feed_override / min_segment_mm / power_percent
    behave as in emit_cut_gcode_simple. warmup_ms_first (if set) is the
    dwell on the very first cut, when the diode is coldest; later cuts use
    the shorter warmup_ms."""
    laser = material["laser"]
    pct = power_percent if power_percent is not None else laser["power_percent"]
    power_s = int(round(pct * 10))
    feed = feed_override if feed_override is not None else laser["feed_mm_per_min"]
    passes = laser["passes"]
    on = "M3" if mode == "static" else "M4"
    warmup_s = max(0, warmup_ms) / 1000.0
    first_ms = warmup_ms if warmup_ms_first is None else warmup_ms_first
    first_s = max(0, first_ms) / 1000.0

    edges = extract_unique_edges(pieces)
    letter_polys = [p["polygon"] for p in pieces if p["kind"] == "letter"]
    letters, interior, panel = [], [], []
    for e in edges:
        cat = classify_edge(e, letter_polys, cfg)
        if cat == "letter":
            letters.append(e)
        elif cat == "panel":
            panel.append(e)
        else:
            interior.append(e)

    start_pt = (cfg.margin_px, cfg.margin_px)
    ordered = (
        greedy_order(letters, start_pt)
        + greedy_order(interior, start_pt)
        + greedy_order(panel, start_pt)
    )

    # Convert each ordered edge to machine mm (honoring greedy_order's
    # direction), then fuse contiguous edges into single continuous cuts so
    # the laser doesn't lift / re-dwell / re-fire mid-line. tol is in mm.
    ordered_mm = [
        (
            LineString([img_to_machine_mm(x, y, cfg) for x, y in edge.coords]),
            rev,
        )
        for edge, rev in ordered
    ]
    chains = chain_contiguous_paths(ordered_mm, tol_px=0.1)

    extra = [
        "loose-fit: centerline cuts, laser kerf becomes the clearance",
        "edge dedup: shared cell-cell boundaries cut exactly once",
        "cut order: letters first, interior next, panel border last",
        "contiguous edges fused into single continuous cuts (no per-edge re-fire)",
        "ASSUMES Z already at focal height in your WCS",
    ]
    if first_ms > 0 or warmup_ms > 0:
        extra.append(
            f"warmup: G4 P{first_s:.3f} on first cut, P{warmup_s:.3f} on the rest "
            "(diode cold-start)"
        )
    if min_segment_mm > 0:
        extra.append(f"min segment: {min_segment_mm}mm (shorter chords decimated)")

    lines = _header(
        title=f"full puzzle: word={word}, {cfg.panel_mm:.0f}x{cfg.panel_mm:.0f}mm, "
        f"{len(chains)} continuous cut paths ({len(ordered)} edges after dedup)",
        material_id=material["id"],
        extra=extra,
        mode=mode,
    )

    first_cut = True
    for idx, path_mm in enumerate(chains, start=1):
        coords_mm = decimate_min_segment(path_mm, min_segment_mm)
        if len(coords_mm) < 2:
            continue
        x0, y0 = coords_mm[0]
        lines.append(f"; --- path {idx}/{len(chains)} ({len(coords_mm)} pts) ---")
        lines.append(f"G0 X{x0:.3f} Y{y0:.3f}")
        lines.append(f"{on} S{power_s}")
        lines.append(f"F{feed}")
        dwell_s = first_s if first_cut else warmup_s
        if dwell_s > 0:
            lines.append(f"G4 P{dwell_s:.3f}  ; warmup")
        first_cut = False
        for pass_n in range(passes):
            if passes > 1:
                lines.append(f"; pass {pass_n + 1} of {passes}")
            if pass_n > 0:
                lines.append(f"G0 X{x0:.3f} Y{y0:.3f}")
                lines.append(f"{on} S{power_s}")
            for x, y in coords_mm[1:]:
                lines.append(f"G1 X{x:.3f} Y{y:.3f}")
        lines.append("M5")
        lines.append("")
    lines += ["G0 X0 Y0", ""]
    return "\n".join(lines)


